#!/usr/bin/env node
/* gen-embedded-fonts.js — generate the DS-owned embedded-font carrier.

   WHY THIS EXISTS
   The raster PNG exporters (patterns/<pattern>/export-png.js) embed each diagram
   @font-face as a base64 data-URI inside the exported SVG, so the SVG-as-image
   raster resolves the same faces the boxes were measured for (see #60). To get
   the woff2 bytes, the exporter fetch()es the font files — which Chromium BLOCKS
   under file://. So a double-clicked diagram (no server) could not export a
   correct PNG; #61 made that case fail CLOSED (block, don't ship a clipped PNG).

   This carrier removes the fetch requirement entirely. It bakes the DS-owned
   woff2 files into a JS global (window.DSA_EMBEDDED_FONTS) that the diagram HTML
   loads BEFORE export-png.js. A JS <script> executes under file:// (unlike a
   cross-origin CSS read or a font fetch), so the exporter can read the same
   base64 the fetch path would have produced — no network, works offline.

   DETERMINISM
   The output is a pure function of colors_and_type.css (@font-face declarations)
   and the fonts/*.woff2 bytes. No timestamps, no randomness — running it twice on
   the same inputs yields byte-identical output. Regenerate whenever a font file or
   an @font-face declaration changes; never hand-edit the carrier.

   PARITY WITH THE EXPORTER
   Faces are parsed with the SAME url regex and the SAME (url|weight|style) dedup
   the exporter's collectFontFaces() uses, and family/weight/style are normalized
   to the CSSOM serialization the exporter reads at runtime, so a carrier-built
   @font-face block is byte-identical to the fetch-built one (http export output
   is unchanged).

   USAGE:  node tools/gen-embedded-fonts.js
   WRITES: fonts-embedded.js  (repo root; vendored downstream as
           _dsa-tokens/fonts-embedded.js alongside _dsa-tokens/colors_and_type.css)
*/
'use strict';
var fs = require('fs');
var path = require('path');

var ROOT = path.resolve(__dirname, '..');
var CSS_PATH = path.join(ROOT, 'colors_and_type.css');
var OUT_PATH = path.join(ROOT, 'fonts-embedded.js');

// Same url matcher the exporter's collectFontFaces() uses: first woff2 in src.
var URL_RE = /url\(\s*['"]?([^'")]+\.woff2)['"]?\s*\)/i;

// Normalize a CSS font-family value to the CSSOM getPropertyValue('font-family')
// serialization the exporter reads at runtime: a family token is emitted
// double-quoted (Chromium serializes 'Inter' → "Inter", 'JetBrains Mono' →
// "JetBrains Mono"). Keeps the carrier-built @font-face byte-identical to the
// fetch-built one.
function normalizeFamily(raw) {
  var s = String(raw == null ? '' : raw).trim();
  if ((s[0] === '"' || s[0] === "'") && s[s.length - 1] === s[0]) {
    s = s.slice(1, -1);
  }
  return '"' + s + '"';
}

function firstDecl(block, prop, dflt) {
  var re = new RegExp(prop + '\\s*:\\s*([^;]+);', 'i');
  var m = re.exec(block);
  return m ? m[1].trim() : dflt;
}

function main() {
  var css = fs.readFileSync(CSS_PATH, 'utf8');
  var faceRe = /@font-face\s*\{([^}]*)\}/gi;
  var faces = [];
  var m;
  while ((m = faceRe.exec(css)) !== null) {
    var block = m[1];
    var src = firstDecl(block, 'src', '');
    var um = URL_RE.exec(src);
    if (!um) continue; // no woff2 src — skip, mirrors collectFontFaces
    faces.push({
      urlPath: um[1],
      family: normalizeFamily(firstDecl(block, 'font-family', '')),
      weight: firstDecl(block, 'font-weight', 'normal'),
      style: firstDecl(block, 'font-style', 'normal'),
    });
  }

  // Dedup by (url|weight|style), preserving declaration order — same key the
  // exporter's collectFontFaces() dedups on.
  var seen = Object.create(null);
  var out = [];
  for (var i = 0; i < faces.length; i++) {
    var k = faces[i].urlPath + '|' + faces[i].weight + '|' + faces[i].style;
    if (seen[k]) continue;
    seen[k] = true;
    out.push(faces[i]);
  }

  var entries = [];
  var totalWoff2 = 0;
  for (var j = 0; j < out.length; j++) {
    var f = out[j];
    var fontPath = path.join(ROOT, f.urlPath);
    var buf = fs.readFileSync(fontPath);
    totalWoff2 += buf.length;
    var b64 = buf.toString('base64');
    // Fixed key order (family, style, weight, b64) for deterministic output.
    entries.push(
      '  {"family":' + JSON.stringify(f.family) +
      ',"style":' + JSON.stringify(f.style) +
      ',"weight":' + JSON.stringify(f.weight) +
      ',"b64":' + JSON.stringify(b64) + '}'
    );
  }

  var banner =
    '/* fonts-embedded.js — GENERATED by tools/gen-embedded-fonts.js. DO NOT EDIT.\n' +
    '   Regenerate: node tools/gen-embedded-fonts.js\n' +
    '   Source: colors_and_type.css @font-face + fonts/*.woff2 (design-system-ASK).\n' +
    '\n' +
    '   Bakes the DS-owned diagram web fonts into a JS global so the PNG exporter\n' +
    '   can embed them WITHOUT fetching woff2 files — the fetch path Chromium blocks\n' +
    '   under file://. Load this BEFORE export-png.js. The exporter still fails\n' +
    '   CLOSED if this carrier is absent AND the fonts cannot be fetched. */\n';

  var body = banner +
    'window.DSA_EMBEDDED_FONTS = [\n' +
    entries.join(',\n') + '\n' +
    '];\n';

  fs.writeFileSync(OUT_PATH, body);

  process.stderr.write(
    'gen-embedded-fonts: ' + out.length + ' faces, ' +
    totalWoff2 + ' woff2 bytes → ' + OUT_PATH + ' (' + Buffer.byteLength(body) + ' bytes)\n'
  );
  out.forEach(function (f) {
    process.stderr.write('  · ' + f.family + ' ' + f.style + ' ' + f.weight + '  ← ' + f.urlPath + '\n');
  });
}

main();
